package projeto_mercado;

public class Biscoitos {
    
    public int Belvita(int quantidade){
        int valor_belvita = quantidade * 2;
        return valor_belvita;
    }
    
    public int Nesfit(int quantidade){
        int valor_nesfit = quantidade * 3;
        return valor_nesfit;
    }
    
    public int Trakinas(int quantidade){
        int valor_trakinas = quantidade * 1;
        return valor_trakinas;
    }
}
